        public void showDiseaseList() {

        DiseaseModel = (DefaultTableModel) DiseaseTable.getModel();
        DiseaseList = DiseaseManage.DiseaseList();

        WellCome.setText("Wellcome "+ AccName);
        
        for (int i = 0; i < DiseaseList.size(); i++) {
            Disease d = (Disease) DiseaseList.get(i);
            Vector row = new Vector();
            row.add(d.getId());
            row.add(d.getName());
  
            DiseaseModel.addRow(row);
        }

    }
    
